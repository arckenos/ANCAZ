    public void actualizarTabla(){
        String col[]={"Concepto", "Descripcion", "Unidades", "Precio", "Total"};
        DefaultTableModel modelo = new  DefaultTableModel(col,0);
        float total = 0;
        Object [] obj = new Object [5];
        List ls;
        
        try {
            ls = detalles;            
            for(int i=0; i<ls.size(); i++){
                Detalleorden detalle = (Detalleorden)ls.get(i);
                obj[0]= detalle.getIdArticulo().getNombre();
                obj[1]= detalle.getIdArticulo().getDescripcion();
                obj[2]= detalle.getCantidad();
                obj[3]= detalle.getIdArticulo().getPrecio();                
                obj[4]= detalle.getIdArticulo().getPrecio() * detalle.getCantidad();
                total += detalle.getIdArticulo().getPrecio() * detalle.getCantidad();
                modelo.addRow(obj);
            }
            this.tblFactura.setModel(modelo);            
            lbTotal.setText(String.format("%.2f", total));
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al cargar datos");
            e.printStackTrace();
        }
    }

