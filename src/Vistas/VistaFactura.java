    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed
        
        
        if(detalles.isEmpty()){
            JOptionPane.showMessageDialog(this, "No se permite registar una orden vacia");
            return;
        }
            
        try {
            // TODO add your handling code here:
            orden.setTotal(Float.parseFloat(lbTotal.getText()));
            Control.registrarOrden(orden, detalles);
        } catch (Exception ex) {
            Logger.getLogger(VistaFactura.class.getName()).log(Level.SEVERE, null, ex);
        }finally{
            JOptionPane.showMessageDialog(this,"Orden registrada en el sistema");            
        }
        vistaOrdenes vo = new vistaOrdenes();
        dispose();
        vo.setVisible(true);
    }//GEN-LAST:event_btnAceptarActionPerformed

