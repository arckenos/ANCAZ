    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        Proveedores proveedores = Control.consultarProveedordoNombre(comboProveedores.getSelectedItem().toString());     
        Proyectos proyectos = Control.consultarProyecto(comboProyectos.getSelectedItem().toString());
        Empleados empleados = Control.consultarEmpleadoNombre(comboEmpleados.getSelectedItem().toString());
        
        Ordenes orden = new Ordenes();
        orden.setIdProveedor(proveedores);
        orden.setIdEmpleado(empleados);
        orden.setIdProyecto(proyectos);
        orden.setFecha(Timestamp.valueOf(LocalDateTime.now()));
        VistaFactura vistaFactura = new VistaFactura(orden);
        dispose();
        vistaFactura.setVisible(true);
        
    }//GEN-LAST:event_jButton1ActionPerformed

