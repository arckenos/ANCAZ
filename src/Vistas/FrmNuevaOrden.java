    public void iniciarCombos(){        
        List<Empleados> empleados = Control.consultarEmpleados();
        for (Empleados empleado : empleados) {
            comboEmpleados.addItem(empleado.getNombre());
        }
        List<Proveedores> proveedoreses = Control.consultarProveedores();
        for (Proveedores proveedorese : proveedoreses) {            
            comboProveedores.addItem(proveedorese.getNombre());
        }
        List<Proyectos> proyectoses = Control.consultarProyectos();
        for (Proyectos proyectose : proyectoses) {
            comboProyectos.addItem(proyectose.getNombre());
        }       
        
        
    }
    
